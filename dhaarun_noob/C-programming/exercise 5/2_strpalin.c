#include <stdio.h>
void main(){
    char stack[100];
    char str[100];
    int sp = 0;

    printf("\nEnter a string : ");
    scanf("%[^\n]",&str);
    printf("\nThe string : ");
    int i = 0,length = printf("%s",str);
    while(i < length){
        if(i < length/2){
            stack[sp++] = str[i];
            printf("\nStack : %s",stack);
        }
        else if(length%2 != 0 && i == length/2){
            i++;
            continue;
            
        }
        else{
            if(stack[--sp] == str[i]){
                printf("\nStack : %s",stack);
            }
            else{
                printf("\nNot a palindrome bitch");
                return;
            }
        }
        i++;
    }
    printf("\n%s is a palindrome",str);
}
